    protected class VertexArray {
        // based on idea seen in https://ieeexplore.ieee.org/document/5655719 (acessed on 5/7/19) - using different method but inspiration
        private List<Vector3> float_indices; // used for maintaining order of vertices, improtant for creating mesh vertex array
        private Dictionary<Vector3,int[]> source; // used for quick look up

        public int Registor(Vector3 vertex, int owner_index, int position_in_mesh, bool use_dictionary = true) {
            float_indices.Add(vertex);
            if ( use_dictionary ) {
                source.Add(vertex, new int[]{owner_index, position_in_mesh});
            }
            return float_indices.Count;
        }
        public int[] Query(Vector3 index) {
            int[] result;
            bool state = source.TryGetValue(index, out result);
            if (!state) {
                return null;
            }
            return result;
        }

        public Vector3[] ToVertex(){
            return float_indices.ToArray();
        }

        public Stopwatch index_time = new Stopwatch();

        public int ToIndex(Vector3 index){
            index_time.Start();// TIME TESTINg
            for ( int i = 0 ; i < float_indices.Count ; i++ ) {
                if ( float_indices[i] == index ) {
                    index_time.Stop(); // TIME TESTING
                    return i;
                }
            }
            index_time.Stop(); // TIME TESTING
            return -1;
        }

        public VertexArray() {
            source = new Dictionary<Vector3,int[]>();
            float_indices = new List<Vector3>();
        }

    }

