            // finding value of each vertices
            float[] distances = {0,0,0,0,0,0,0,0}; 
            bool[] in_metaball = new bool[8]; 
            for ( int i = 0 ; i < animal_parts.Length ; i++ ) {
                for ( int j = 0 ; j < vertices.Length ; j++ ) {
                    distances[j] = distances[j] + animal_parts[i].evaluated_distance(vertices[j]);
                }
            }
            for ( int i = 0 ; i < distances.Length ; i++ ) {
                if ( distances[i] >= 1 ) {
                    in_metaball[i] = true;
                }
                else {
                    in_metaball[i] = false;
                }
            }
            int lookup_value = 0;
            for ( int i = 0 ; i < in_metaball.Length ; i++ ) {
                if (in_metaball[i]) {
                    lookup_value = lookup_value + (int)(System.Math.Pow(2,i));
                }
            }

            //finding vertices
            int edge_intersections = edgeTable[lookup_value];
            int vertices_counter = 0;
            for (int i = 0; i < System.Math.Log(edge_intersections, 2); i++)
            {
                if (((int)(edge_intersections / System.Math.Pow(2, i)) % 2) == 1)
                {
                    vertices_counter++;
                }
            }
            var mesh_vertices_list = new Vector3[vertices_counter];
            var mesh_vert_nums = new int[vertices_counter];
            vertices_counter = 0;
            float linear_gradient; // for linear interpolation
            int A, B;
            for (int i = 0 ; i < System.Math.Log(edge_intersections,2) ; i++ ) {
                if ( ((int)(edge_intersections / System.Math.Pow(2,i)) % 2) == 1) {
                    if ( i < 4 ) {
                        A = i;
                        B = ( i + 1 ) % 4;
                    }
                    else if ( i < 8 ) {
                        A = i;
                        B = ( i + 1 ) % 8;
                        if ( B == 0 ) {
                            B = B + 4;
                        }
                    }
                    else {
                        A = i % 8;
                        B = A + 4;

                    }
                    if ( interpolation ) {
                        linear_gradient = ( 1 - distances[A] ) / ( distances[B] - distances[A] );
                    }
                    else {
                        linear_gradient = 0.5F;
                    }
                    Vector3 new_vertex = vertices[A] + (vertices[B] - vertices[A]) * linear_gradient;
                    mesh_vertices_list[vertices_counter] = new_vertex;
                    mesh_vert_nums[vertices_counter] = i;
                    vertices_counter++;
                } 
            }

            // finding triangles
            var facets = new List<int>();
            for ( int i = 0 ; i < 16 ; i++ ) {
                if ( triTable[lookup_value,i] != -1 ) {
                    facets.Add(triTable[lookup_value,i]);
                }
                else {
                    i = 16;
                }
            }
            int[] mesh_triangles = new int[facets.Count];
            for (int i = 0 ; i < facets.Count ; i++ ) {
                for ( int j = 0 ; j < mesh_vert_nums.Length ; j++ ) {
                    if (facets[i] == mesh_vert_nums[j]) {
                        mesh_triangles[facets.Count - 1 - i] = j;
                    }
                }
            }
            int index;
            triangle_interface = new int[mesh_triangles.Length];
            for ( int i = 0 ; i < mesh_triangles.Length ; i++ )
